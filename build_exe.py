def compile_iss(iss_path: Path):
    iscc = r"C:\Program Files (x86)\Inno Setup 6\ISCC.exe"
    if not Path(iscc).exists():
        print("Inno Setup Compiler (ISCC.exe) not found. Skipping compilation.")
        print(f"  Expected at: {iscc}")
        print("  You can manually compile: ISCC.exe setupInno.iss")
        return False
    print("\n" + "=" * 60)
    print("  Compiling Inno Setup installer...")
    print("=" * 60)
    subprocess.check_call([iscc, str(iss_path)], cwd=ROOT)

    exe_name = f"{APP_NAME}-Setup-v{APP_VERSION}.exe"
    installer = DIST / exe_name
    if installer.exists():
        print(f"\nInstaller created: {installer}")
    return True


def main():
    parser = argparse.ArgumentParser(description="Build Archiver EXE + Installer")
    parser.add_argument("--setup", action="store_true", help="Build EXE + compile .iss installer")
    parser.add_argument("--iss", action="store_true", help="Generate .iss only (requires existing EXE in dist/)")
    parser.add_argument("--exe-only", action="store_true", help="Build EXE only (default)")
    args = parser.parse_args()

    if args.iss:
        exe_dir = DIST / f"{APP_NAME.lower()}"
        generate_iss(exe_dir)
        return

    if args.setup:
        exe_dir = build_exe()
        iss_path = generate_iss(exe_dir)
        compile_iss(iss_path)
        print(f"\n{'=' * 60}")
        print(f"  Done! Installer: {DIST / f'{APP_NAME}-Setup-v{APP_VERSION}.exe'}")
        print(f"  EXE: {exe_dir / f'{APP_NAME.lower()}.exe'}")
        print(f"{'=' * 60}")
        return

    # Default: build EXE only
    exe_dir = build_exe()
    generate_iss(exe_dir)
    print(f"\n{'=' * 60}")
    print(f"  Done! EXE: {exe_dir / f'{APP_NAME.lower()}.exe'}")
    print(f"  .iss: {ROOT / 'setupInno.iss'}")
    print(f"  Run 'python build_exe.py --setup' to compile the installer.")
    print(f"{'=' * 60}")


if __name__ == "__main__":
    main()
