#!/usr/bin/env python3
"""
Build EXE + Inno Setup Installer for Archiver
Usage:
    python build_exe.py              # build EXE only
    python build_exe.py --iss        # generate .iss only
    python build_exe.py --setup      # build EXE + compile .iss
"""

import argparse
import os
import shutil
import subprocess
import sys
from pathlib import Path

ROOT = Path(__file__).parent.resolve()
DIST = ROOT / "dist"
BUILD = ROOT / "build_temp"
ARCH_PKG = ROOT / "arch"
APP_NAME = "Archiver"
APP_VERSION = "0.2.0"
PUBLISHER = "Archiver Team"


def clean_build():
    for d in [BUILD]:
        if d.exists():
            shutil.rmtree(d)
    for p in DIST.glob("*.spec"):
        p.unlink()


def create_runtime_hook() -> Path:
    hook = BUILD / "_arch_hook.py"
    hook.parent.mkdir(parents=True, exist_ok=True)
    hook.write_text(r'''"""PyInstaller runtime hook — redirects data paths to install dir."""
import sys
from pathlib import Path

if getattr(sys, "frozen", False):
    app_dir = Path(sys.executable).parent.resolve()
    import arch.config as _cfg
    _cfg.BASE_DIR = app_dir
    _cfg.DATA_DIR = app_dir / "data"
    _cfg.DB_PATH = _cfg.DATA_DIR / "arch.db"
    _cfg.LOG_PATH = _cfg.DATA_DIR / "arch.log"
    _cfg.KEY_PATH = _cfg.DATA_DIR / ".arch_key"
    _cfg.ARCHIVES_DIR = _cfg.DATA_DIR / "archives"
    _cfg.BACKUP_DIR = _cfg.DATA_DIR / "backups"
''', encoding="utf-8")
    return hook


def build_exe():
    print("=" * 60)
    print(f"  Building {APP_NAME} v{APP_VERSION} EXE")
    print("=" * 60)

    clean_build()
    DIST.mkdir(parents=True, exist_ok=True)
    hook = create_runtime_hook()

    entry = BUILD / "_arch_entry.py"
    entry.write_text('''"""Entry point for PyInstaller bundle."""
import sys
from pathlib import Path

if getattr(sys, "frozen", False):
    sys.path.insert(0, str(Path(sys.executable).parent))

import threading
import webbrowser

def _start_web(host="127.0.0.1", port=8080):
    threading.Timer(1.5, lambda: webbrowser.open(f"http://{host}:{port}")).start()
    from arch.web import serve
    serve(host=host, port=port)

if len(sys.argv) <= 1:
    _start_web()
elif sys.argv[1] == "web":
    host = "127.0.0.1"
    port = 8080
    for i, arg in enumerate(sys.argv[2:], 2):
        if arg == "--host" and i + 1 < len(sys.argv):
            host = sys.argv[i + 1]
        elif arg == "--port" and i + 1 < len(sys.argv):
            try:
                port = int(sys.argv[i + 1])
            except ValueError:
                pass
    _start_web(host, port)
else:
    from arch.__main__ import main
    main()
''', encoding="utf-8")

    cmd = [
        sys.executable, "-m", "PyInstaller",
        "--onedir",
        "--name", f"{APP_NAME.lower()}",
        "--distpath", str(DIST),
        "--workpath", str(BUILD / "pyi_work"),
        "--specpath", str(BUILD),
        "--add-data", f"{ARCH_PKG}{os.pathsep}arch",
        "--add-data", f"{ROOT / 'arch.ico'}{os.pathsep}.",
        "--add-data", f"{ROOT / 'readme.txt'}{os.pathsep}.",
        "--icon", str(ROOT / "arch.ico"),
        "--runtime-hook", str(hook),
        "--noconfirm",
        "--clean",
        str(entry),
    ]

    subprocess.check_call(cmd, cwd=ROOT)

    exe_dir = DIST / f"{APP_NAME.lower()}"
    if not (exe_dir / f"{APP_NAME.lower()}.exe").exists():
        print("ERROR: EXE not found after build!")
        sys.exit(1)

    print(f"\nEXE built: {exe_dir / f'{APP_NAME.lower()}.exe'}")
    return exe_dir


